/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     ListNode *next;
 *     ListNode() : val(0), next(nullptr) {}
 *     ListNode(int x) : val(x), next(nullptr) {}
 *     ListNode(int x, ListNode *next) : val(x), next(next) {}
 * };
 */
class Solution {
public:
    static int getDecimalValue(ListNode* head, int ans=0) {
        return (!head)?ans:getDecimalValue(head->next, (ans<<1)+head->val);
    }
};
